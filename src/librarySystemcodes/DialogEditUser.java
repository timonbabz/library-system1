public void updateUser(){
        
        String userIdnumber = txtUid.getText();
        String firstname = txtFname.getText();
        String lastname = txtLname.getText();
        String department = txtDept.getText();
        String paermmssion = txtPermission.getText();
        
        if(userIdnumber.isEmpty()){
            JOptionPane.showMessageDialog(null, "ID cannot be empty");}
        else if(firstname.isEmpty()){
            JOptionPane.showMessageDialog(null, "enter first name");}
        else if(lastname.isEmpty()){
            JOptionPane.showMessageDialog(null, "enter last name");}
        else if(department.isEmpty()){
            JOptionPane.showMessageDialog(null, "enter department");}
        else{
                try {
            String url = "jdbc:mysql://localhost/libsystem?useSSL = false";
            Connection conn;
            conn = DriverManager.getConnection(url, "root", "libsystem@dmin");
            
            //--------update users db-----------
            String sql = "UPDATE users_db SET user_fname='" + firstname + "',user_lname='" + lastname + "',department='" + department + "'"
                    + ",permission='" + paermmssion + "' WHERE id_no=" + userIdnumber;
            
            PreparedStatement pst;
            pst = null;
            pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Updated successfully");
            
        txtUid.setText("");
        txtFname.setText("");
        txtLname.setText("");
        txtUsernam.setText("");
        txtDept.setText("");
        txtPermission.setText("");
        comboPermission.setSelectedIndex(0);
            
            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(null, e);
            }
            dispose();
        }
    }

