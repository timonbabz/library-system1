            Connection conn;
            conn = DriverManager.getConnection(url, "root", "libsystem@dmin");
            
            //--------update users db-----------
            String sql = "UPDATE users_db SET password='" +  md5(txtpasswordReset.getPassword()) + "', reset='" + resetText + "' WHERE id_no=" + userIdnumber;
            
