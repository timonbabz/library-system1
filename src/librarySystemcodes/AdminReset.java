    private void btnResetPassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetPassActionPerformed
        String userIdnumber = txtID.getText();
        String firstname = txtFname.getText();
        String userName = txtUser.getText();
        String lastname = txtLname.getText();
        String department = txtDept.getText();
        String passReset = new String (txtpasswordReset.getPassword());
        
        if(userIdnumber.isEmpty()){
            JOptionPane.showMessageDialog(null, "ID cannot be empty");
        txtID.requestFocus();}
        else if(userName.isEmpty()){
            JOptionPane.showMessageDialog(null, "Username required");
        txtUser.requestFocus();}
        else if(userName.equalsIgnoreCase("support")){
            JOptionPane.showMessageDialog(null, "Editing not allowed for username 'support'");
        txtUser.requestFocus();}
        else if(firstname.isEmpty()){
            JOptionPane.showMessageDialog(null, "enter first name");
        txtFname.requestFocus();}
        else if(lastname.isEmpty()){
            JOptionPane.showMessageDialog(null, "enter last name");
        txtLname.requestFocus();}
        else if(department.isEmpty()){
            JOptionPane.showMessageDialog(null, "enter department");
        txtDept.requestFocus();}
         else if(passReset.length() < 6){
            JOptionPane.showMessageDialog(null, "password cannot be less than six(6) characters");
         txtpasswordReset.requestFocus();}
         else if(passReset.equalsIgnoreCase("password")){
         JOptionPane.showMessageDialog(null, "password cannot be (password)");
         txtpasswordReset.requestFocus();}
        else {resetPass();}
    }//GEN-LAST:event_btnResetPassActionPerformed

