    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        int formno = Integer.parseInt(txtForm.getText().trim());
        String formName = (String) boxStream.getSelectedItem();
        if(txtAdm.getText().isEmpty()){
        JOptionPane.showMessageDialog(null, "Enter admission number");
        txtAdm.requestFocus();}
        if(txtFname.getText().isEmpty()){
        JOptionPane.showMessageDialog(null, "Enter First name");
        txtFname.requestFocus();}
        if(txtLname.getText().isEmpty()){
        JOptionPane.showMessageDialog(null, "Enter Last name");
        txtLname.requestFocus();}
        if(formName.equalsIgnoreCase("default")){
        JOptionPane.showMessageDialog(null, "Select stream");
        txtForm.requestFocus();}
        if(txtStream.getText().equalsIgnoreCase("default")){
        JOptionPane.showMessageDialog(null, "Select stream");
        txtStream.requestFocus();}
        else{
            int response = JOptionPane.showConfirmDialog(null, "Add "+txtFname.getText()+" ("+txtAdm.getText()+") to student records?",
                "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        switch (response) {
            case JOptionPane.NO_OPTION:
                break;
            case JOptionPane.CLOSED_OPTION:
                break;
            case JOptionPane.YES_OPTION:
        addStudent();}
        }
    }//GEN-LAST:event_btnSaveActionPerformed

