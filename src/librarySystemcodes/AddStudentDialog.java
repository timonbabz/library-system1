    public void addStudent(){
    
        String idnumber = txtAdm.getText();
        String fname = txtFname.getText().toUpperCase();
        String lname = txtLname.getText().toUpperCase();
        String formNumber = txtForm.getText();
        String stream = txtStream.getText();
        
                try {
                    Class.forName("com.mysql.jdbc.Driver");

                    con = DriverManager.getConnection("jdbc:mysql://localhost/libdb", "root", "libsystem@dmin");
                    statement = con.createStatement();

                    // SQL Insert
                    String sql = "INSERT INTO student_list " + "(adm_no, std_fname, std_lname, form, stream)"
                            + "VALUES ('" + idnumber + "','"
                            + fname + "','"
                            + lname + "','"
                            + formNumber + "','"
                            + stream + "') ";
                    
                    statement.execute(sql);

                    JOptionPane.showMessageDialog(null, "Student added successfully");

                    // Reset Text Fields
                    txtAdm.setText("");
                    txtFname.setText("");
                    txtLname.setText("");
                    txtForm.setText("");
                    txtStream.setText("");
                    //status  = true;

                } catch (ClassNotFoundException | SQLException | HeadlessException es) {
                    // TODO Auto-generated catch block
                    JOptionPane.showMessageDialog(null, "Unable to add");
                    JOptionPane.showMessageDialog(null, es.getMessage());
                }

                try {
                    if (statement != null) {
                        statement.close();
                        con.close();
                    }
                } catch (SQLException es) {
                    // TODO Auto-generated catch block
                    JOptionPane.showMessageDialog(null, es.getMessage());
                }
        //end of if statement that validate the form
}
    
    public void loadcomboForm() {
    try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/libdb", "root", "libsystem@dmin");
            PreparedStatement st = con.prepareStatement("SELECT form FROM form");
            ResultSet rs = st.executeQuery();
            while(rs.next()){                            
                boxForm.addItem(rs.getString("form"));
                }
            con.close();
            }
        catch(Exception e)
        {
            //System.out.println("Error"+e);
        }    
}
    
