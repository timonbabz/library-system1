        public void updatestaffDetails() { 
        
            String staffStatus = (String) comboUpdate.getSelectedItem();
            try {
                String url = "jdbc:mysql://localhost/libdb?useSSL = false";
                Connection conn;
                conn = DriverManager.getConnection(url, "root", "libsystem@dmin");
               
                //--------update student db-----------
                String sql = "UPDATE staff_table SET id_number='" + txtId.getText() + "',"
                        + "staff_fname='" + txtFname.getText().toUpperCase() + "' ,staff_lname='" + txtLname.getText().toUpperCase() + "', dept='"+ txtDept.getText().toUpperCase() +"', phone_no='"+ txtDept.getText().toUpperCase() +"', status='"+ staffStatus +"' WHERE (id_number='"+ txtId.getText() +"')";
                
                PreparedStatement pst;
                pst = null;
                pst = conn.prepareStatement(sql);
                pst.execute();
                
                //-----clear text fields after above------------
                
                JOptionPane.showMessageDialog(null, "Staff record updated");
                txtId.setText("");
                txtFname.setText("");
                txtLname.setText("");
                txtDept.setText("");
                txtPhoneNo.setText("");
                dispose();

            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        
    }
    
